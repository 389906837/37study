package com.cloud.cang.security.vo;

public class PurviewVO {

	private String purviewId;
	private String purviewCode;//权限码
	private String purviewUrl;
	
	public String getPurviewId() {
		return purviewId;
	}
	public void setPurviewId(String purviewId) {
		this.purviewId = purviewId;
	}
	public String getPurviewCode() {
		return purviewCode;
	}
	public void setPurviewCode(String purviewCode) {
		this.purviewCode = purviewCode;
	}
	public String getPurviewUrl() {
		return purviewUrl;
	}
	public void setPurviewUrl(String purviewUrl) {
		this.purviewUrl = purviewUrl;
	}
}
